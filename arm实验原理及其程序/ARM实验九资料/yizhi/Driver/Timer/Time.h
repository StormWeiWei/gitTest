/*********************************************************************************************************
** 函数名称：uint8 T0MAT_Init(uint32 time,uint32 PR_data,uint8 T_MODE,uint8 EXT_MODE,uint8 MATn,uint8 Int_En)
** 功能描述：定时器0匹配模式初始化。
** 入口参数：time		匹配时间，该值会直接写入到匹配寄存器中
**			 PR_data	预分频寄存器的值
**			 T_MODE		匹配控制模式
**						0:匹配时，定时器复位
**						1:匹配时，定时器停止
**			 EXT_MODE	匹配时，外部输出控制
**						0:不执行任何动作
**						1:外部匹配输出0
**						2:外部匹配输出1
**						3:外部匹配输出翻转
**			 MATn		匹配通道选择，0～3
**			 Int_En		中断使能
**						0:发生匹配事件时，不产生中断
**						1:发生匹配事件时，产生中断
** 入口参数：0	初始化失败
**           1	初始化成功
** 说明：调用前，要设置相关的引脚
********************************************************************************************************/
