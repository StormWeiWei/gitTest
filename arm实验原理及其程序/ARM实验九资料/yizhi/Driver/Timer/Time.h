#define		Read_T0()	T0TC		//读取定时器0的计数值
#define		Read_T1()	T1TC		//读取定时器1的计数值
/*********************************************************************************************************
** 函数名称：uint8  T0CAP_Init(uint8 CAP_MODE,uint32 PR_data,uint8 CAPn,uint8 Int_En)
** 功能描述：定时器0捕获模式初始化。
** 入口参数：CAP_MODE	捕获方式，按位操作方式。
**						bit0	1--CAP上跳沿捕获
**						bit1	1--CAP下降沿捕获
**			 PR_data	预分频寄存器的值
**			 CAPn		捕获通道选择，0～3
**			 Int_En		中断使能
**						0:发生捕获事件时，不产生中断
**						1:发生捕获事件时，产生中断
** 出口参数：1 	初始化成功
**         	 0 	初始化失败
** 说明：调用前，要设置相关的引脚。
********************************************************************************************************/
extern  uint8  T0CAP_Init(uint8 CAP_MODE,uint32 PR_data,uint8 CAPn,uint8 Int_En);


