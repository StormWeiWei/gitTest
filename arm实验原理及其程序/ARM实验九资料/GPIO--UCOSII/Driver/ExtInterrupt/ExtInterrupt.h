#include "config.h"

#define	CleanExtIntFlg(no)	EXTINT &= (1 << no)	// 清零指定的外部中断标志
												// no为：0～3
#define	IsExtInt(no)		(EXTINT & (1<<no))	// 判断是否产生指定的外部中断
												// no为：0～3
												// 返回值 0  没产生外部中断
												// 返回值>0  产生了外部中断	
/*********************************************************************************************************
** 函数名称：uint8 SetExtInt(uint8 no, uint8 mode, uint8 bWakeUp)
** 函数功能：设置外部中断属性
** 入口参数：no			外部中断，取值0～3
**			 mode		中断触发类型，取值如下：
**                      0：低电平触发	1：高电平触发
**                      2：下降沿触发	3：上升沿触发
**			 bWakeUp	是否使能中断唤醒CPU。
**						0	不唤醒CPU
**						>0	使能唤醒CPU
** 出口参数：0	参数错误
**			 1	操作成功
** 说明：需要在外部设置引脚模式。
********************************************************************************************************/
extern uint8 SetExtInt(uint8 no, uint8 mode, uint8 bWakeUp);
