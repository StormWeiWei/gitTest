#include "config.h"

#define  BEEPCON     1<<7       //P0.7脚控制蜂鸣器

/*******************************************************************************************************
**名称：DelayNS
**功能：延时函数                        
********************************************************************************************************/
void DelayNS(uint32 dly)
{
    uint32 i;
    for(;dly > 0; dly --)
    {
        for(i = 0; i < 50; i++);
    }
}

/*******************************************************************************************************
**名称：main
**功能：控制蜂鸣器叫
**调试说明：将SmartSOPC教学实验开发平台上的A6区的JP7短接,把B2区的JP6的BEEP短接起来。                           
********************************************************************************************************/
int main (void)
{
    PINSEL0 = 0;		  //设置引脚连接GPIO
    IO0DIR = BEEPCON;     //设置IO口
    
    while(1)
    {
        IO0SET = BEEPCON;   //BEEPCON = 1
        DelayNS(2);
        IO0CLR = BEEPCON;   //BEEPCON = 0
        DelayNS(2);
    }
    
}
