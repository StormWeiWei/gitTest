#include "config.h"

/*********************************************************************************************************
** 函数名称：uint8	ADC_Init(uint8 ADn,uint32	Fadc)
** 功能描述：AD初始化操作。
** 入口参数：ADn	AD通道，0~7
**			 Fadc	ADC的转换时钟,最大的转换速率为4.5M,即，4500000
** 出口参数：0 	初始化失败
**           1	初始化成功
** 说明：转换时钟最大4.5MHz。
**		 使用软件方式启动。
********************************************************************************************************/
extern  uint8	ADC_Init(uint8 ADn,uint32  Fadc);


/*********************************************************************************************************
** 函数名称：uint32	Read_ADC(uint8 ADn)
** 功能描述：读取AD转换值。
** 入口参数：ADn	AD通道，0~7
** 出口参数：读取出来的AD结果
** 说明：软件采用查询方式等待转换结束,同时已经将结果经过了处理，转换结果为：0～3ff。
**		 如果参数有误，转换结果为0。
********************************************************************************************************/
extern  uint32	Read_ADC(uint8 ADn);


